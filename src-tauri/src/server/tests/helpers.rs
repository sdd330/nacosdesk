/**
 * 测试辅助函数
 * 提供测试用的辅助函数和测试数据
 */

/// 创建测试用的命名空间 ID（默认为 public）
pub fn test_namespace_id() -> String {
    "public".to_string()
}

/// 创建测试用的服务组名（默认为 DEFAULT_GROUP）
pub fn test_group_name() -> String {
    "DEFAULT_GROUP".to_string()
}

/// 创建测试用的配置 Data ID
pub fn test_data_id() -> String {
    "test-config".to_string()
}

/// 创建测试用的服务名
pub fn test_service_name() -> String {
    "test-service".to_string()
}

